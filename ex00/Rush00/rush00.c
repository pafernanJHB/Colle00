#include	<unistd.h>
#include	"ft_putchar.c"

int		rush(int a)
{	
	if (a == 0 || a > a - 1)
	{
		ft_putchar('o');
	}
	else if(a <= a - 1 || a > 0)
	{
		ft_putchar('-');
	}
	else if(a < 0)
	{
		return	(0);
	}
	a--;
	return	(0);
}
